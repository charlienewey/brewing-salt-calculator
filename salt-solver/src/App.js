const steps = ['Source profile input', 'Target profile input', 'Review your changes'];

function getStepContent(step, selectRow) {
  switch (step) {
    case 0:
      return (
        <React.Fragment>
          <Typography variant="h6" gutterBottom>
            Source Water Profile Input
          </Typography>
          <br />
          <MineralEntry classNames='SourceProfileEntry'></MineralEntry>
        </React.Fragment>
      );
    case 1:
      return (
        <React.Fragment>
          <Typography variant="h6" gutterBottom>
            Target Water Profile Input
          </Typography>
          <br />
          <MineralEntry classNames='TargetProfileEntry'></MineralEntry>
          <br />
          <Accordion>
            <AccordionSummary
              expandIcon={<ExpandMoreIcon />}
              aria-controls="panel2a-content"
              id="panel2a-header"
            >
              Water Profile Presets
            </AccordionSummary>
            <AccordionDetails>
              <WaterProfilesTable
                selectRow={selectRow}
              >
              </WaterProfilesTable>
            </AccordionDetails>
          </Accordion>
        </React.Fragment>
      );
    case 2:
      return <Review />;
    default:
      throw new Error('Unknown step');
  }
}

export default function App() {
  const classes = useStyles();
  const [activeStep, setActiveStep] = React.useState(0);

  const selectRow = (row) => {
    alert(row);
    // this.setState({clickedRow: row});
  };

  const handleNext = () => {
    setActiveStep(activeStep + 1);
  };

  const handleBack = () => {
    setActiveStep(activeStep - 1);
  };

  return (
    <React.Fragment>
      <CssBaseline />
      <main className={classes.layout}>
        <Paper className={classes.paper}>
          <Typography component="h1" variant="h4" align="center">
            Brewing Salt Calculator
          </Typography>

          <Stepper activeStep={activeStep} className={classes.stepper}>
            {steps.map((label) => (
              <Step key={label}>
                <StepLabel>{label}</StepLabel>
              </Step>
            ))}
          </Stepper>

          <React.Fragment>
            {activeStep === steps.length ? (
              <React.Fragment>
                <Typography variant="h5" gutterBottom>
                  Thank you for your order.
                </Typography>
                <Typography variant="subtitle1">
                  Your order number is #2001539. We have emailed your order confirmation, and will
                  send you an update when your order has shipped.
                </Typography>
              </React.Fragment>
            ) : (
              <React.Fragment>
                {getStepContent(activeStep, this.selectRow)}
                <div className={classes.buttons}>
                  {activeStep !== 0 && (
                    <Button onClick={handleBack} className={classes.button}>
                      Back
                    </Button>
                  )}
                  <Button
                    variant="contained"
                    color="primary"
                    onClick={handleNext}
                    className={classes.button}
                  >
                    {activeStep === steps.length - 1 ? 'Print Summary' : 'Next'}
                  </Button>
                </div>
              </React.Fragment>
            )}
          </React.Fragment>
        </Paper>
        <Copyright />
      </main>
    </React.Fragment>
  );
}